/*
For each track, how many sectors are in that track.
*/
const int sectorsPerZone[5] = { 12, 11, 10, 9, 8 };


/*
    In a block file, logical blocks for the track 1 side 1 (12 sectors) are stored,
    followed by 12 sectors for side 2.
    Repeat.

    To find offset for a particular track, sector, side:
    zone = track / 16
    zoneTrackIndex = track % 16
    offset = zoneOffset[zone] + (zoneTrackIndex * 2 + side) * sectorsPerZone[zone];
*/

const int zoneOffset[5] = { 0, 192*2, 368*2, 528*2, 672*2 };

int16_t calculateSectorOffset(int track, int sector, int side) {
    int zone = track / 16;
    int zoneTrackIndex = track % 16;
    return zoneOffset[zone] + (zoneTrackIndex * 2 + side) * sectorsPerZone[zone] + sector;
}

